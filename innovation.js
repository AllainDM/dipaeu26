// Сортировка по алфавиту внутри категорий
for (let key in innovations) {
    innovations[key].sort((a, b) => a.name.localeCompare(b.name));
}

const selects = document.querySelectorAll('.inv-select');
const displayResults = document.getElementById('display-results');

function init() {
    selects.forEach(select => {
        const cat = select.getAttribute('data-cat');
        
        // Дефолтная опция
        select.add(new Option("- выберите инновацию -", ""));
        
        innovations[cat].forEach((inv, index) => {
            select.add(new Option(inv.name, index));
        });

        select.addEventListener('change', render);
    });
    render();
}

function render() {
    let finalHtml = "";
    let hasAnyData = false;

    // Проходим по каждой категории отдельно для группировки
    for (const [catKey, catName] of Object.entries(catNames)) {
        let groupHtml = `<h4>${catName}</h4><table class="res-table">`;
        let countInGroup = 0;

        // Ищем все селекторы этой категории
        const groupSelects = document.querySelectorAll(`.inv-select[data-cat="${catKey}"]`);
        
        groupSelects.forEach(select => {
            if (select.value !== "") {
                const data = innovations[catKey][select.value];
                groupHtml += `
                    <tr>
                        <td style="width: 30%; font-weight: bold;">${data.name}</td>
                        <td>${data.effect}</td>
                    </tr>`;
                countInGroup++;
                hasAnyData = true;
            }
        });

        groupHtml += `</table>`;

        // Добавляем группу в финальный HTML только если там что-то выбрано
        if (countInGroup > 0) {
            finalHtml += groupHtml;
        }
    }

    displayResults.innerHTML = hasAnyData 
        ? finalHtml 
        : "<p style='color: #666;'>Ничего не выбрано. Выберите инновации в выпадающих списках выше.</p>";
}

document.addEventListener('DOMContentLoaded', () => {
    // Находим все выпадающие списки инноваций
    const selects = document.querySelectorAll('.inv-select');

    selects.forEach(select => {
        select.addEventListener('wheel', function(event) {
            // Блокируем прокрутку самой страницы, когда курсор над селектором
            event.preventDefault();

            // event.deltaY > 0 — крутим колесико вниз
            // event.deltaY < 0 — крутим колесико вверх
            if (event.deltaY > 0) {
                if (this.selectedIndex < this.options.length - 1) {
                    this.selectedIndex++;
                }
            } else {
                if (this.selectedIndex > 0) {
                    this.selectedIndex--;
                }
            }

            // Важно: вызываем событие 'change' вручную.
            // Без этого твоя функция обновления эффектов (в блоке #display-results) не узнает об изменениях.
            this.dispatchEvent(new Event('change'));
        }, { passive: false }); // Обязательно для работы preventDefault
    });
});

// Запуск при загрузке
document.addEventListener('DOMContentLoaded', init);